package Egitura;

public class AldaketaEgitura {
	public int errenkada;
	public int zutabea;
	public int balioa;
	public int hautagaia;

	public AldaketaEgitura() {}

	public AldaketaEgitura(int errenkada, int zutabea, int balioa, int hautagaia) {
		this.errenkada = errenkada;
		this.zutabea = zutabea;
		this.balioa = balioa;
		this.hautagaia = hautagaia;
	}
}
